<template>
  <div class="admin pa-6">
    <v-container>
      <v-toolbar flat color="transparent">
        <p class="text-h4 text-uppercase">Admin Users List</p>
        <v-spacer></v-spacer>
        <v-text-field
          v-model="search"
          append-icon="mdi-magnify"
          label="Search User"
          hide-details
          class="subtitle-1"
          style="max-width: 200px;"
        ></v-text-field>
        <button-dialog icon="mdi-plus" label="New User" width="600">
          <admin-form :update="false"></admin-form>
        </button-dialog>
      </v-toolbar>
      <v-data-table
        :headers="headers"
        :items="adminTable"
        :search="search"
        :items-per-page="10"
      >
        <template v-slot:top>
          <v-toolbar flat color="primary" height="40px" dark>
            <p class="subtitle-1">Total Accounts: {{ admin.length }}</p>
          </v-toolbar>
        </template>
        <template v-slot:[`item.role`]="{ item }">
          <v-select :items="adminRoles" item-text="text" item-value="value" dense v-model="item.role" @change="updateAdminRole(item.id, item.role)"></v-select>
        </template>
        <template v-slot:[`item.status`]="{ item }">
          <v-select :items="adminStatus" dense v-model="item.status" @change="updateAdminStatus(item.id, item.status)"></v-select>
        </template>
        <template v-slot:[`item.actions`]="{ item }" class="actions">
          <table-action-dialog icon="mdi-pencil" width="600">
            <admin-form :update="true" :admin-to-update="item"></admin-form>
          </table-action-dialog>
          <v-btn icon :to="`admin/logs/${item.id}`" target="_blank">
            <v-icon small>mdi-format-list-bulleted</v-icon>
          </v-btn>
        </template>
      </v-data-table>
    </v-container>
  </div>
</template>

<script>
import { mapState } from "vuex";

export default {
  name: "Admin",
  components: {
    ButtonDialog: () => import("../../component/ButtonDialog"),
    TableActionDialog: () => import("../../component/TableActionDialog"),
    AdminForm: () => import("../../components/forms/AdminForm")
  },
  data: () => ({
    headers: [
      { text: "USERNAME", value: "email", width: "15%" },
      { text: "FULL NAME", value: "full_name", width: "15%" },
      { text: "ROLE", value: "role" },
      { text: "STATUS", value: "status" },
      { text: "CREATED DATE", value: "created_date", width: "15%" },
      { text: "LAST ACCESS DATE", value: "last_access_date", width: "15%" },
      {
        text: "OPTIONS",
        value: "actions",
        width: "10%",
        align: "center",
        sortable: false
      }
    ],
    search: ""
  }),
  computed: {
    ...mapState("admin", ["admin", "adminRoles", "adminStatus"]),
    adminTable() {
      let adminTable = []
      this.admin.map(admin => {
        let full_name = `${admin.first_name} ${admin.last_name}`
        let adminObject = { ...admin }
        this.$set(adminObject, 'full_name', full_name)
        adminTable.push(adminObject)
      })
      return adminTable
    }
  },
  methods: {
    updateAdminRole(id, role) {
      this.$store.commit('admin/UPDATE_ADMIN_ROLE', { id, role })
    },
    updateAdminStatus(id, status) {
      this.$store.commit('admin/UPDATE_ADMIN_STATUS', { id, status })
    }
  }
};
</script>

